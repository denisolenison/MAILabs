		function progonka(matrix, vectorB) {
			var vectorX = [];
			
			var N = vectorB.length;
			
			var alphas = [];
			var betas = [];
			
			
			for (var i = 0 ; i < vectorB.length ; ++i) {
				alphas.push(0);
				betas.push(0);
			}
		
			//Прямой ход прогонки
		
			for (var i = 0; i < N; ++i) {
				var A0, C0, B0, F0;
		
				if (i - 1 < 0) {
					A0 = 0;
				}
				else {
					A0 = matrix[i][i - 1];
				}
		
				C0 = -1 * matrix[i][i];
		
				if (i + 1 < N) {
					B0 = matrix[i][i + 1];
				}
				else {
					B0 = 0;
				}
		
				F0 = vectorB[i];
		
		
				if (i == 0) {
					alphas[i] = B0 / C0;
					betas[i] = -(F0 / C0);
				}
				else if (i == N - 1) {
					alphas[i] = 0;
					betas[i] = (betas[i - 1] * A0 - F0) / (C0 - alphas[i - 1] * A0);
				}
				else {
					alphas[i] = B0 / (C0 - alphas[i - 1] * A0);
					betas[i] = (betas[i - 1] * A0 - F0) / (C0 - alphas[i - 1] * A0);
				}
		
			}
		
		
			vectorX[N - 1] = betas[N - 1];
		
			for (var i = 2; i <= N; ++i) {
				vectorX[N - i] = alphas[N - i] * vectorX[N - i + 1] + betas[N - i];
			}
			
			return vectorX;
		}
