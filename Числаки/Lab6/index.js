		function explicit_step(i, vector0, vector1, vector2) {
			var curT = i * t;
					
			//Сначала вычисляем серединку
			for (var j = 1 ; j < N ; ++j) {
				//данную формулу я долго выводил на бумаге, аппроксимируя
				//производные
				
				vector2[j] = (-2*vector1[j]*(t*t - h*h - t*h*h - t*t*h
					+ 1.5*t*t*h*h) - vector0[j]*h*h + vector1[j+1]*t*t
					- vector1[j-1]*t*t*(2*h-1)) / (h*h*(1 + 2*t));
				
				
				
					
				maxVals[i] = (vector1[j] > maxVals[i] ? vector2[j] : maxVals[i]);
				minVals[i] = (vector1[j] < minVals[i] ? vector2[j] : minVals[i]);
			}
					
			//аппроксимируем граничные условия
			//Граничные условия 1 рода аппроксимируются с беск. порядком
			//Поэтому, выбор метода аппроксимации гр. усл. не меняет ничего
			
			
			vector2[0] = phi_0(curT)/beta;
			vector2[N] = 0;
			
					
			maxVals[i] = (vector2[0] > maxVals[i] ? vector2[0] : maxVals[i]);
			maxVals[i] = (vector2[N] > maxVals[i] ? vector2[N] : maxVals[i]);
			
			minVals[i] = (vector2[0] < minVals[i] ? vector2[0] : minVals[i]);
			minVals[i] = (vector2[N] < minVals[i] ? vector2[N] : minVals[i]);
			
			maxMaxVal = (maxVals[i] > maxMaxVal ? maxVals[i] : maxMaxVal);
			minMinVal = (minVals[i] < minMinVal ? minVals[i] : minMinVal);
					
			return copyV(vector2);
			
		}
		
		
		
		function implicit_step(i, vector0, vector1, vector2) {
			var curT = i * t;
					
			var solveMatrix = [];
			var solveVector = [];
					
			var emptV = [];
			for (var j = 0 ; j <= N ; ++j) {
				emptV.push(0);
			}
					
			var thisV = copyV(emptV);
					
			thisV[0] = beta
			thisV[1] = 0;
						
			solveMatrix.push(copyV(thisV));
						
			var d0 = phi_0(curT);
						
			solveVector.push(d0); //d0
			
					
			for (var j = 1 ; j < N ; ++j) {
				var thisV = copyV(emptV);
				thisV[j - 1] = -1/(h*h);
				thisV[j] = 1/(t*t) + 2/t + 2/(h*h) + 2/h + 3;
				thisV[j + 1] = -1/(h*h) - 2/h;
						
				solveMatrix.push(copyV(thisV));
						
				var dj = (2*vector1[j] - vector0[j])/(t*t) + 2*vector1[j]/t;
				solveVector.push(dj);
						
						
			}
					
			var thisV = copyV(emptV);
				
			
			thisV[N-1] = 0;
			thisV[N] = delta;
				
			solveMatrix.push(copyV(thisV));
						
			var dn = 0;
						
			solveVector.push(dn); //d1
			
					
			var the_vector = progonka(solveMatrix, solveVector);
					
					
			for (var j = 0 ; j <= N ; ++j) {
			
				maxVals[i] = (the_vector[j] > maxVals[i] ? the_vector[j] : maxVals[i]);
				minVals[i] = (the_vector[j] < minVals[i] ? the_vector[j] : minVals[i]);
				
				maxMaxVal = (maxVals[i] > maxMaxVal ? maxVals[i] : maxMaxVal);
				minMinVal = (minVals[i] < minMinVal ? minVals[i] : minMinVal);
					
			}
			
			return copyV(the_vector);
		}
		
		
		
